/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

	// Settings
	int NUM_AXES = 3;           // Number of axes on accelerometer
	int MAX_MEASUREMENTS = 32; // Number of samples to keep in each axis
	float MAD_SCALE = 1.4826;   // Scale MAD to be inline with calcs
	float THRESHOLD = 1e-03;    // Any MSE over this is an anomaly
	int SAMPLE_RATE = 200;      // How fast to collect measurements (Hz)

	float sample[MAX_MEASUREMENTS][NUM_AXES];
	float measurements[NUM_AXES][MAX_MEASUREMENTS];
	float y_val[MAX_MEASUREMENTS];
	float mse[NUM_AXES];

	char buf[50];
	int buf_len = 0;
	ai_error ai_err;
	ai_network_report report;
	ai_i32 nbatch;
	uint32_t timestamp;
	int16_t pDataXYZ[3];

	//float y_val;

	// Memoria per tenere valori intermedi della rete
	ai_u32 activations[AI_NETWORK_DATA_ACTIVATIONS_SIZE];

	// Buffer per input ed output tensor
	float in_data[AI_NETWORK_IN_1_SIZE];
	float out_data[AI_NETWORK_OUT_1_SIZE];

	/* AI buffer IO handlers */
	ai_buffer *ai_input;
	ai_buffer *ai_output;

	// Puntatore al modello
	ai_handle network = AI_HANDLE_NULL;

	// Imposta memoria e pesi
	ai_network_params ai_params = { AI_NETWORK_DATA_WEIGHTS(
			ai_network_data_weights_get()), AI_NETWORK_DATA_ACTIVATIONS(
			activations) };

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI2_Init();
  MX_USART2_UART_Init();
  MX_USB_HOST_Init();
  MX_CRC_Init();
  MX_TIM16_Init();
  /* USER CODE BEGIN 2 */
	HAL_TIM_Base_Start(&htim16);

	BSP_COMPASS_Init();

	// Welcome
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_2, GPIO_PIN_SET);
	HAL_Delay(500);
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_2, GPIO_PIN_RESET);

	buf_len = sprintf(buf, "\r\n\r\nSTM X-Cube-AI LSTM v1.0\r\n");
	HAL_UART_Transmit(&huart2, (uint8_t*) buf, buf_len, 100);

	// rete
	ai_err = ai_network_create(&network, AI_NETWORK_DATA_CONFIG);
	if (ai_err.type != AI_ERROR_NONE) {
		buf_len = sprintf(buf, "ERROR: could not create NN instance\r\n");
		HAL_UART_Transmit(&huart2, (uint8_t*) buf, buf_len, 100);
		while (1)
			;
	}
	if (!ai_network_init(network, &ai_params)) {
		buf_len = sprintf(buf, "ERROR: could not init NN instance\r\n");
		HAL_UART_Transmit(&huart2, (uint8_t*) buf, buf_len, 100);
		while (1)
			;
	}
	if (ai_network_get_report(network, &report) != true) {
		buf_len = sprintf(buf, "ai get report error\r\n");
		HAL_UART_Transmit(&huart2, (uint8_t*) buf, buf_len, 100);
		while (1)
			;
	}

	// puntatori ai buffer dati
	ai_input = ai_network_inputs_get(network, NULL);
	ai_output = ai_network_outputs_get(network, NULL);

	ai_input[0].data = AI_HANDLE_PTR(in_data);
	ai_output[0].data = AI_HANDLE_PTR(out_data);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1) {
		// Take a given time worth of measurements
		int i = 0;
		while (i < MAX_MEASUREMENTS) {
			timestamp = htim16.Instance->CNT;
			if (timestamp >= 5) {
				// Take sample measurement
				BSP_COMPASS_AccGetXYZ(pDataXYZ);
				sample[i][0] = (double) pDataXYZ[0] * 0.000061;
				sample[i][1] = (double) pDataXYZ[1] * 0.000061;
				sample[i][2] = (double) pDataXYZ[2] * 0.000061;

				buf_len = sprintf(buf, "%f, %f, %f, %lu ms\r\n", sample[i][0],
						sample[i][1], sample[i][2], timestamp);
				HAL_UART_Transmit(&huart2, (uint8_t*) buf, buf_len, 100);

				// Update sample counter
				i++;
				htim16.Instance->CNT = 0;
			}
		}

		// For each axis, prepare input buffer
		for (int axis = 0; axis < NUM_AXES; axis++) {
			for (int i = 0; i < MAX_MEASUREMENTS; i++) {
				in_data[axis + i * NUM_AXES] = sample[i][axis];
				measurements[axis][i] = sample[i][axis];
			}
		}

		htim16.Instance->CNT = 0;

		nbatch = ai_network_run(network, ai_input, ai_output);
		if (nbatch != 1) {
			buf_len = sprintf(buf, "ERROR: could not run inference\r\n");
			HAL_UART_Transmit(&huart2, (uint8_t*) buf, buf_len, 100);
		}

		uint32_t inferenceTime = htim16.Instance->CNT;

		// Read predicted y value from output buffer (tensor)
		for (int axis = 0; axis < NUM_AXES; axis++) {
			for (int i = 0; i < MAX_MEASUREMENTS; i++) {
				y_val[i] = ((float*) out_data)[axis + i * NUM_AXES];
			}
			mse[axis] = calc_mse(measurements[axis], y_val, MAX_MEASUREMENTS);
		}

		buf_len = sprintf(buf, "Duration inference: %lu ms\r\n", inferenceTime);
		HAL_UART_Transmit(&huart2, (uint8_t*) buf, buf_len, 100);

		// Calculate MSE between given and predicted values
		buf_len = sprintf(buf, "MSE: x:%f, y:%f, z:%f\r\n", mse[0], mse[1],
				mse[2]);
		HAL_UART_Transmit(&huart2, (uint8_t*) buf, buf_len, 100);

		// Compare to threshold
		if (mse[0] > THRESHOLD || mse[1] > THRESHOLD || mse[2] > THRESHOLD) {
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_2, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOE, GPIO_PIN_8, GPIO_PIN_RESET);
			buf_len = sprintf(buf, "\033[1;31m DANGER!\r\n\033[1;37m");
			HAL_UART_Transmit(&huart2, (uint8_t*) buf, buf_len, 100);
		} else {
			HAL_GPIO_WritePin(GPIOE, GPIO_PIN_8, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_2, GPIO_PIN_RESET);
			buf_len = sprintf(buf, "\033[0;32m ALL OK!\r\n\033[1;37m");
			HAL_UART_Transmit(&huart2, (uint8_t*) buf, buf_len, 100);
		}

    /* USER CODE END WHILE */
    MX_USB_HOST_Process();

    /* USER CODE BEGIN 3 */
	}
  /* USER CODE END 3 */
}

